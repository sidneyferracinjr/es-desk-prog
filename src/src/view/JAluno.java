    public void Navega(int indice){
        try {
            Aluno alu = controllerAluno.getAlunos().get(indice);
            jTextFieldNome.setText(alu.getNome());
            jTextFieldMatricula.setText(Integer.toString(alu.getMatricula()));
            jTextFieldTurma.setText(alu.getTurma());
        } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Não há mais alunos nessa direção: " + e.getMessage(),
                                      "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }
    
public void adicionarAluno() {
    try {
        Aluno alu = new Aluno();
        alu.setNome(jTextFieldNome.getText());
        alu.setMatricula(Integer.parseInt(jTextFieldMatricula.getText()));
        alu.setTurma(jTextFieldTurma.getText());
        controllerAluno.getAlunos().add(alu);

        JOptionPane.showMessageDialog(null, "Aluno adicionado com sucesso!", 
                                      "Sucesso", JOptionPane.INFORMATION_MESSAGE);

        jTextFieldNome.setText("");
        jTextFieldMatricula.setText("");
        jTextFieldTurma.setText("");
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Por favor, insira uma matrícula válida.", 
                                      "Erro de Entrada", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Ocorreu um erro ao adicionar o aluno: " + e.getMessage(),
                                      "Erro", JOptionPane.ERROR_MESSAGE);
    }
}

    
    public void alterarAlunoPorCampos() {
        try {
            String nome = jTextFieldNome.getText();
            int matricula = Integer.parseInt(jTextFieldMatricula.getText());
            String turma = jTextFieldTurma.getText();

            boolean alterado = controllerAluno.alterarAluno(matricula, nome, matricula, turma);

            if (alterado) {
                JOptionPane.showMessageDialog(null, "Aluno alterado com sucesso!", 
                                              "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Aluno com matrícula " + matricula + " não encontrado.", 
                                              "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Matrícula inválida. Por favor, insira um número válido.", 
                                          "Erro de Entrada", JOptionPane.WARNING_MESSAGE);
        }
    }

    
