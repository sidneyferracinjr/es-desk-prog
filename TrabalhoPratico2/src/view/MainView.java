    private void selectDirectory() throws IOException {
        directoryPath = mainController.selectDirectory(tfDirectoryPath.getText());
        
        if (directoryPath == null) {
            showMessage(null, "O caminho da pasta n√£o pode ser vazio.", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            describeDirectory(mainController.describeDirectory(directoryPath));
            tfDirectoryPath.setText(directoryPath);
        }
    }
    
    private void describeDirectory(String selectedDirectory) {
        if (taSelectedDirectory == null) {
            taSelectedDirectory = new JTextArea();
            taSelectedDirectory.setEditable(false);
            taSelectedDirectory.setLineWrap(true);
            taSelectedDirectory.setWrapStyleWord(true);

            spSelectedDirectory = new JScrollPane(taSelectedDirectory);
            add(spSelectedDirectory);
        }

        taSelectedDirectory.setText(selectedDirectory);

        validate();
        repaint();
    }
    
